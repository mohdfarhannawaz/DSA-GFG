class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int num = N;
        int count = 0;
         int div;
        while(num>0){
            div = num%10;
            if(div != 0 && N % div == 0){
                count++;
            }
            num = num / 10;
        }
        return count;
    }
};

